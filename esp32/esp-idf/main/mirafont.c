void mira_fb_clear(uint8_t *fb){ memset(fb, 0, MIRA_FB_SIZE); }

void mira_fb_pixel(uint8_t *fb, int x, int y, int on){
  uint16_t idx; uint8_t bit;
  if (x < 0 || x >= MIRA_FB_W || y < 0 || y >= MIRA_FB_H) return;
  idx = (uint16_t)x + (uint16_t)(y >> 3) * MIRA_FB_W;
  bit = (uint8_t)(1u << (y & 7));
  if (on) fb[idx] |= bit; else fb[idx] &= (uint8_t)~bit;
}

const uint8_t *mira_glyph(uint32_t cp){
  int i;
  if (cp >= MIRA_ASCII_FIRST && cp < (uint32_t)(MIRA_ASCII_FIRST + MIRA_ASCII_COUNT))
    return MIRA_GLYPHS[cp - MIRA_ASCII_FIRST];
  for (i = 0; i < MIRA_ACCENT_COUNT; i++)
    if (MIRA_ACCENT_CP[i] == cp) return MIRA_ACCENT_GLYPHS[i];
  return (const uint8_t *)0;
}

int mira_char_width(int scale){ if (scale < 1) scale = 1; return 6 * scale; }
int mira_text_width(const char *s, int scale){ return (int)strlen(s) * mira_char_width(scale); }

int mira_draw_glyph(uint8_t *fb, int x, int y, const uint8_t rows[MIRA_ROWS], mira_style_t style, int scale){
  int r, c, dx, dy, ds, off, px, py; uint8_t rb;
  if (scale < 1) scale = 1;
  ds = scale; off = 0;
  if (style == MIRA_DOTS && scale >= 2){ ds = scale - 1; if (ds < 1) ds = 1; off = (scale - ds) / 2; }
  for (r = 0; r < MIRA_ROWS; r++){
    rb = rows[r];
    for (c = 0; c < MIRA_COLS; c++){
      if ((rb >> (4 - c)) & 1){
        px = x + c * scale + off; py = y + r * scale + off;
        for (dy = 0; dy < ds; dy++)
          for (dx = 0; dx < ds; dx++)
            mira_fb_pixel(fb, px + dx, py + dy, 1);
      }
    }
  }
  return 6 * scale;
}

int mira_draw_char(uint8_t *fb, int x, int y, char c, mira_style_t style, int scale){
  const uint8_t *g = mira_glyph((uint32_t)(unsigned char)c);
  if (!g) return 6 * (scale < 1 ? 1 : scale);
  return mira_draw_glyph(fb, x, y, g, style, scale);
}

int mira_draw_text(uint8_t *fb, int x, int y, const char *s, mira_style_t style, int scale){
  int x0 = x;
  while (*s) x += mira_draw_char(fb, x, y, *s++, style, scale);
  return x - x0;
}

int mira_draw_utf8(uint8_t *fb, int x, int y, const char *s, mira_style_t style, int scale){
  const unsigned char *p = (const unsigned char *)s; int x0 = x; uint32_t cp; const uint8_t *g;
  if (scale < 1) scale = 1;
  while (*p){
    if (*p < 0x80){ cp = *p; p += 1; }
    else if ((*p & 0xE0) == 0xC0 && p[1]){ cp = ((uint32_t)(*p & 0x1F) << 6) | (p[1] & 0x3F); p += 2; }
    else if ((*p & 0xF0) == 0xE0 && p[1] && p[2]){ cp = ((uint32_t)(*p & 0x0F) << 12) | ((uint32_t)(p[1] & 0x3F) << 6) | (p[2] & 0x3F); p += 3; }
    else { p += 1; continue; }
    g = mira_glyph(cp);
    if (g) mira_draw_glyph(fb, x, y, g, style, scale);
    x += 6 * scale;
  }
  return x - x0;
}

void mira_draw_bitmap(uint8_t *fb, int x, int y, const uint8_t *bmp, int w, int h){
  int r, c, bytesPerRow = (w + 7) / 8;
  for (r = 0; r < h; r++)
    for (c = 0; c < w; c++){
      uint8_t byte = bmp[r * bytesPerRow + (c >> 3)];
      if ((byte >> (7 - (c & 7))) & 1) mira_fb_pixel(fb, x + c, y + r, 1);
    }
}
